public class CompanyBean {
    private String companyName;
    private String area;
    private String address;
    private int companyType;
    private String companySizes;
    private String companyDesc;
    private String emily;
    private String phone;

    public String getCompanyDesc() {
        return companyDesc;
    }

    public void setCompanyDesc(String companyDesc) {
        this.companyDesc = companyDesc;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getCompanyType() {
        return companyType;
    }

    public void setCompanyType(int companyType) {
        this.companyType = companyType;
    }

    public String getCompanySizes() {
        return companySizes;
    }

    public void setCompanySizes(String companySizes) {
        this.companySizes = companySizes;
    }

    public String getEmily() {
        return emily;
    }

    public void setEmily(String emily) {
        this.emily = emily;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
